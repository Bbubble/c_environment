void set_RelayCom(const char *p)
{
  for(char i=0;i<20;i++)
   {
     Serial.print(*p++);
   }
}

void setup()  
{ 
  int rate = 9600;
  Serial.begin(rate); 
  while (!Serial)
  {
    ; // wait for serial port to connect. Needed for Leonardo only
  }
}

void loop() 
{
   set_RelayCom(D0_LOW);
   set_RelayCom(D1_LOW);
   set_RelayCom(D2_LOW);
   set_RelayCom(D3_LOW);
   delay(1000);
   set_RelayCom(D0_HIGH);
   set_RelayCom(D1_HIGH);
   set_RelayCom(D2_HIGH);
   set_RelayCom(D3_HIGH);
   delay(1000);
}

